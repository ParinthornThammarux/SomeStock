ticker_entry = ttk.Entry(root, width=20)
ticker_entry.pack(pady=5)

# ‡∏Å‡∏•‡πà‡∏≠‡∏á‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•
result_text = tk.Text(root, height=10, width=40, wrap="word")
